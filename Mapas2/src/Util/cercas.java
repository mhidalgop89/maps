package Util;

public class cercas {
	
	int idCercas;
	String nombreCerca;
	String descripcionCerca;
	String estado;
	String usuario;
	int idUsuario;
	public int getIdCercas() {
		return idCercas;
	}
	public void setIdCercas(int idCercas) {
		this.idCercas = idCercas;
	}
	public String getNombreCerca() {
		return nombreCerca;
	}
	public void setNombreCerca(String nombreCerca) {
		this.nombreCerca = nombreCerca;
	}
	public String getDescripcionCerca() {
		return descripcionCerca;
	}
	public void setDescripcionCerca(String descripcionCerca) {
		this.descripcionCerca = descripcionCerca;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public int getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(int idUsuario) {
		this.idUsuario = idUsuario;
	}
	

}
