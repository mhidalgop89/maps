package Util;

import java.util.Date;



public class Mapas {
	
	int idGmap;
	String nombreGmap;
	Date fecha;
	
	public Date getFecha() {
		return fecha;
	}
	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}
	public int getIdGmap() {
		return idGmap;
	}
	public void setIdGmap(int idGmap) {
		this.idGmap = idGmap;
	}
	public String getNombreGmap() {
		return nombreGmap;
	}
	public void setNombreGmap(String nombreGmap) {
		this.nombreGmap = nombreGmap;
	}
	

}
