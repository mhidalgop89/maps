package Util;

public class Grupo {

	int gr_Id;
	String gr_descripcion;
	String gr_value;
	boolean selected;
	
	
	public boolean isSelected() {
		return selected;
	}
	public void setSelected(boolean selected) {
		this.selected = selected;
	}
	public String getGr_descripcion() {
		return gr_descripcion;
	}
	public void setGr_descripcion(String gr_descripcion) {
		this.gr_descripcion = gr_descripcion;
	}
	public int getGr_Id() {
		return gr_Id;
	}
	public void setGr_Id(int gr_Id) {
		this.gr_Id = gr_Id;
	}
	public String getGr_value() {
		return gr_value;
	}
	public void setGr_value(String gr_value) {
		this.gr_value = gr_value;
	}
	

	
	
	
	
}
