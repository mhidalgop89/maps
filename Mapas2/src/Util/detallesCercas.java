package Util;

public class detallesCercas {
	
	double longitud;
	double latitud;
	double orden;
	int id_detalle_cerca;
	int id_cerca;
	int idUsuario;
	
	
	public int getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(int idUsuario) {
		this.idUsuario = idUsuario;
	}
	public double getLongitud() {
		return longitud;
	}
	public void setLongitud(double longitud) {
		this.longitud = longitud;
	}
	public double getLatitud() {
		return latitud;
	}
	public void setLatitud(double latitud) {
		this.latitud = latitud;
	}
	public double getOrden() {
		return orden;
	}
	public void setOrden(double orden) {
		this.orden = orden;
	}
	public int getId_detalle_cerca() {
		return id_detalle_cerca;
	}
	public void setId_detalle_cerca(int id_detalle_cerca) {
		this.id_detalle_cerca = id_detalle_cerca;
	}
	public int getId_cerca() {
		return id_cerca;
	}
	public void setId_cerca(int id_cerca) {
		this.id_cerca = id_cerca;
	}
	
	
	

}
